public class 位运算符 {
    public static void main(String[] args) {
        //10的二进制位为---->00001010
        //10的二进制位为右移一位是---->00000101
        System.out.println(10>>1);//10的二进制位右移一位就是除以2
        System.out.println(10<<1);//10的二进制位左移一位就是乘以2
    }
}
