public class 测试此字符串是否以指定的后缀结束 {
    public static void main(String[] args) {
        // boolean endsWith(String suffix) 测试此字符串是否以指定的后缀结束。
        System.out.println("hello".endsWith("o"));//true

        //判断某个子字符串是否以某个字符串开始
        System.out.println("hello".startsWith("h"));//true


    }
}
