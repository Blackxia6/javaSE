package String类里的方法;

/**
 * @date 2022/9/15 - 16:30
 */
public class charAt方法 {
    public static void main(String[] args) {
       // charAt(int index )返回指定索引处的 char 值。
        char c = "中国人".charAt(1);
        System.out.println(c);//国
        char a = "chian".charAt(2);
        System.out.println(a);//i
    }
}
